/* Duplicar el valor numérico de un número dado */
function numeroDuplicado( numero )
{
    let resultado = numero * 2
    return resultado
    // return numero * 2;
}

console.log( numeroDuplicado( 7 ) )

/* Sumar dos números y retornar el resultado */
function sumarNumeros( numeroA, numeroB )
{
    let resultado = numeroA + numeroB
    return resultado
}

console.log( sumarNumeros( 6, 4 ) )
console.log( sumarNumeros( 10, 20 ) )
console.log( sumarNumeros( 30, 2 ) )
